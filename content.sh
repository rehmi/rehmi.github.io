Xtitle: ZeroN levitated interaction element
X-
Xdate:  2011
X-
Xcontent: 
XThe dream of liberating physical matter from gravity has existed for millennia. It is an idea found in mythology, sought after by alchemists, and visualized in science fiction. In this work, we have created a space that gives a glimpse into the possibilities of such a future where materials are free from gravitational constraints by blending the physical and digital worlds.
X
XZeroN is a new tangible interaction element that can be levitated and moved freely by computer in three dimensions. Both the computer and the user can move ZeroN simultaneously, allowing humans  and computers to physically interact with one another in space. Users are invited to place or move the ZeroN just as they can place any other objects on surfaces.
X
XOnce levitated, ZeroN's behavior can be digitally programmed. For example, users can place the sun above physical objects to cast digital shadows, or place a planet that will start revolving based on simulated physical conditions. 
X
XZeroN can also record the user's motion, allowing it to be preserved and played back indefinitely. When a user moves and releases ZeroN, it continues to float and starts to move along the same path, creating a unique, tangible record of the user's physical presence and motion.
X
XTo realize this vision, we developed a magnetic control system that can levitate and actuate a permanent magnet in a pre-defined 3D volume. This is combined with an optical tracking and display system that projects images on the levitating object. Conventional levitation only enables levitating an object at one point. In the ZeroN Project, users can move a levitated object to anywhere in a predefined 3D space and leave it there without dropping it because the system constantly readjusts both magnetically and mechanically.
X
