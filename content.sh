XWe demonstrate a promising type of microfabricated accelerometer that is based on the optical interferometer. The interferometer consists of surface-micromachined interdigital fingers that are alternately attached to a proof mass and support substrate. Illuminating the fingers with coherent light generates a series of diffracted optical beams. Subangstrom displacements between the proof mass and frame are detected by measuring the intensity of a diffracted beam. The structure is fabricated with a two-mask silicon process and detected with a standard laser diode and photodetector. We estimate that the minimum detectable acceleration is six orders of magnitude below the acceleration of gravity, i.e., 2 µg/√Hz in a 1 Hz bandwidth centered at 650 Hz.
