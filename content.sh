XTo achieve these etchings, Muniz worked with Coelho and Rehmi Post to devise a highly technical process involving both antiquated technology and innovative visual tools. Muniz first created sketches of castles using a camera lucida, an optical superimposition device created in 1807 that turns images in front of the viewer into a projection on piece of paper, allowing him to trace the tiny castles.
