Xare meant made to be worn against our bodies from the material we prefer to wear, cloth.
X\end{abstract}
X
X\subsection*{Introduction}
XWearable computational devices, from pagers to entire PC's, are compelling and promise empowerment by 
Xputting a vast array of information right at our fingertips.	
XHowever, their current physical design and bulk prevent them from being comfortable, portable, washable 
Xand pervasively used.  The tactile and material properties of what people wear are important to them, and
Xpeople are reluctant to have wires and hard plastic cases against their bodies.  Ideally, whole computers 
Xwould be made from materials people are comfortable wearing, cloth.  Towards that end, we have used 
Xvarious commercially available conductive fabrics, threads and clothing components (buttons, snaps and 
Xzippers), to create electronic circuits made entirely from fabric.  These circuits provide a means for the 
Xdistribution of data and power, touch sensing and remote sensing, (antennas).  These circuits use sewn 
Xpassive electronic devices, capacitors, resistors, sensors, and conductors to create entire electronic devices, 
Xlike keyboards and touch screens made of cloth.  
X
XSuch cloth electronic devices would give wearers the ability to play music on sewn or embroidered piano 
Xkeys and to jot a note or phone number on a sleeve.  A wearer might pull out a ``handkerboard'' from 
Xhis/her pocket to type a letter into a shoe computer.  If she spills some coffee on the handkerboard, she can 
Xgo home and throw it in the wash.  Cuffs or collars might light to alert wearers of an urgent message.
X
X
X
X\subsection*{Materials}
XFor years the textile industry has been weaving metallic yarns into fabrics for decorative purposes.  These 
Xyarns may include shiny plastic or real metal. The first conductive fabric we explored was silver, silk 
Xorganza, which contains two types of fibers.  On the warp is a plain silk thread.  Running in the other 
Xdirection on the weft is a silk thread wrapped in thin silver foil.  (Figure \ref{fig:organza}). This metallic 
Xyarn is prepared just like cloth-core telephone wire, and has high conductivity.  The silk fiber core has a 
Xhigh tensile strength and can withstand high temperatures.   This strength allows the yarn to be easily sewn 
Xor embroidered with industrial machinery, unlike all-metal wire or fibers that break.  The spacing between 
Xthese fibers allows them to be individually addressed.  In this way, a strip of such fabric can function like a 
Xribbon cable.  
X
XCircuits fabricated on organza only need to be protected from contact with themselves, which can be 
Xaccomplished by coating, supporting or backing the fabric with an insulating layer which can also be cloth.  
XAlso, circuits formed in this fashion have many degrees of flexibility (i.e. they can be wadded up), as 
Xcompared to the single degree of flexibility that conventional substrates can provide.
X
XThere are also conductive yarns manufactured specifically for producing filters for the processing of fine 
Xpowders.  These yarns have conductive and cloth fibers interspersed throughout.  Varying the ratio of the 
Xtwo constituent fibers leads to differences in resistivity.  These fibers can be sewn to create conductive 
Xtraces and resistive elements.  
X
XWhile some components such as resistors, capacitors, and coils can be sewn directly in fabric, there is still a 
Xneed to attach other components to the fabric.  This can be done by soldering directly onto the metallic 
Xyarn. Surface mount LEDs, crystals, piezo transducers, and other surface mount components with pads 
Xspaced more than 0.100 inch apart are easy to solder into the fabric.  Once components are attached, their 
Xconnections to the metallic yarn may need to be mechanically strengthened.  This can be achieved with an 
Xacrylic or other flexible coating.
XComponents with ordinary leads can be sewn directly into circuits on fabric, and specially shaped feet 
Xcould be developed to facilitate this process.
X
XGripper snaps make excellent connectors between the fabric and electronics.  Since the snap pierces the 
Xyarn it creates a surprisingly robust electrical contact.  It also provides a good surface to solder to.  In this 
Xway subsystems can be easily snapped into clothing or removed for washing.
X
X\subsection*{Implementation}
X
XSeveral circuits have been built on and with fabric to date, including busses  to connect various digital 
Xdevices (like pagers and watches),  microcontroller systems that sense proximity and touch, and all-fabric 
Xkeyboards and touchpads.
X
XIn the microcontroller circuit shown in Figure \ref{fig:smartkerchief}, a PIC16C84 and
Xits supporting components are soldered directly onto a square of fabric.
XThe circuit uses the bidirectional I/O pins on the PIC to
Xcontrol LEDs  and to sense touch along the length of the fabric, while providing musical feedback to 
Xreinforce the sense of interaction.
XBuilding systems in this way is easy because components can
Xbe soldered directly onto the conductive yarn. The addressability of conductors in the fabric make it a
Xgood material for prototyping, and it can simply be cut where signals lines are to terminate.
X
XOne kind of fabric keyboard uses pieced conductive and nonconductive fabric, sewn together like a quilt to 
Xmake a row- and column-addressable structure. The quilted conductive columns are insulated from the 
Xconductive rows with a soft, thick fabric, like felt, velvet, or quilt batting. Holes in the insulating fabric 
Xlayer allow the row and column conductors to make contact with each other when pressed. This insulation 
Xalso provides a rewardingly springy, button-like mechanical effect. Contact is made to each row and 
Xcolumn with a gripper snap, and each snap is soldered to a wire which leads to the keyboard encoding 
Xcircuitry.  This keyboard can be wadded up, thrown in the wash, and even used as a potholder if desired.  
XSuch row-and-column structures can also be made by embroidering or silk-screening the contact traces.
X
XKeyboards can also be made in a single layer of fabric using capacitive sensing~~\cite{baxter:capsense}, 
Xwhere an array of embroidered or silk-screened electrodes make up the points of contact.  A finger's contact 
Xwith an electrode can be sensed by measuring the increase in the electrode's total capacitance.  It is worth 
Xnoting that this can be done with a single bidirectional digital I/O pin per electrode, and a leakage resistor 
Xsewn in highly resistive yarn.  Capacitive sensing arrays can also be used to tell how well a piece of 
Xclothing fits the wearer, because the signal varies with pressure..
X 
X\subsection*{Printed sensors}
X
XTo sense changes in environmental factors, the electrical characteristics of an embroidered element can be 
Xmeasured by observing the element's response to a finite impulse of voltage or current.  This response can 
Xbe analyzed to determine changes in impedance, capacitance, and inductance of the element.  This is crucial 
Xin using the fabric to sense a material's response to changes in humidity, temperature, or other factors.
X
XBy layering a row-and-column addressable structure with, for example, a layer of temperature-sensitive 
Xresistive material, one can quickly create an array of temperature sensors addressable through the fabric. 
XThe spatial resolution of the array is limited mainly by the thread pitch of the fabric (approx. 50 to 200 per 
Xinch).
X
X
X\subsection*{Conclusions}
X
XWe have shown how to combine conventional sewing  and electronics techniques with a novel class of 
Xmaterials to create interactive digital devices.  All of the input devices can be made by seamstresses or 
Xclothing factories, entirely from fabric.  These textile-based sensors, buttons, and switches are easy to scale 
Xin size.  They also can conform to any desired shape, which is a great advantage over most existing, 
Xdelicate touch sensors that must remain flat to work at all. Subsystems can be connected together using 
Xordinary textile snaps and fasteners.   Finally, most of what has been described can be thrown in the wash if 
Xsoiled by coffee, food, or sand at the beach.
