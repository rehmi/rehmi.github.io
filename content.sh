Xtitle: Stretchable Cable
X-
Xdate: 2005, Sep
X-
Xcontent: 
XHere we show how to arrange non-elastic elements (wires, fibers, etc.) in a geometry that can extend to several times its resting length. Furthermore, this can be done with existing braiding techniques and materials, obviating the need for specialized materials or equipment.
X
X
XThe resistance of this cable remains constant when it is stretched, unlike other elastic conductors whose resistance is a bulk or surface property. Since the conducting fiber does not itself stretch, the basic properties of the wire do not change as the braid is stretched.
X
X
XWe also can use multiple distinct, adjacent conductors and alternating conductors to form transmission lines with predictable impedance and twisted pairs to increase noise immunity.
XWhen a braid containing a balanced transmission line is stretched, the spacing between conductors decreases, and so does the characteristic impedance of the transmission line.Rehmi Post, Kit Waal
X-
Xkeywords: cable, stretchable, electronic textiles,
X-
XBraided conductors are already known and used in the manufacture of cables, but they do not appear in prior art as structures intended to provide extensible electrical interconnects. Furthermore, in existing practice individual strands of these braids are all used as electrically parallel conductors. In the case of braided shielding, individual conductors in a braid are uninsulated to form an effective shield at a uniform electrical potential. In the case of Litz wire, individual conductors in the braid are insulated to form distinct but parallel paths for high-frequency, high-current signals that would be excluded from the interior of a single,larger conductor.
X
XThe structure of the cable can also be that of a single conductor forming a helical wrap around the inner elastomer, without opposing elements.Stretchable multi-conductor cable: Conductors can be woven into the braid in parallel and thus form a ribbon cable with the unique property of stretchability.Braid permits definition of transmission line structures: - twisted pair is formed in the same braid and stays positioned by nature of the braid - as the braid is stretched, the characteristic impedance changes predictably - by sensing the change in impedance, drive electronics can compensate and also determine degree of extension of the braid.An adaptive impedance matching network may be included at each end to provide proper termination and compensate for variation of transmission line impedance as the braid is stretched.Optical fibers may be incorporated into a braid.
